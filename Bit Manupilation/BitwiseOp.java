import java.util.*;

class BitwiseOp {
public static void main(String[] args) {   //therory in notes 
    //Bitwise AND
    System.out.println(5 & 6);

    //Bitwise OR
    System.out.println(5 | 6);

    //Bitwise XOR
    System.out.println(5 ^ 6);       

    //Bitwise one's complement
    System.out.println(~ 1);

    //Binary left shift
    System.out.println(5 << 2);

    //Binary right shift
    System.out.println(6 >> 1);


}
    
}

